package Unit2;

public class Unit2Task0 {
    public static void main(String[] args) {
        for (int i = 0; i < 11; i++) {
            System.out.print(i+" ");
        }
        System.out.println();

        int a = 0;
        while (a < 11) {
            System.out.print(a+" ");
            a++;
        }
        System.out.println();

        int b = 0;
        do{
            System.out.print(b+" ");
            b++;
        }while (b<11);
    }
}
